package com.example.runcloud.entity;

public class NetizenShowBasic {	
	private String accountId;
	private String account;
	private String category;
	private String isDelete;
	private String createDate;
	private String netizenId;
	private String alarmStatus;
	private String alarmWords;
	
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getIsDelete() {
		return isDelete;
	}
	public void setIsDelete(String isDelete) {
		this.isDelete = isDelete;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getNetizenId() {
		return netizenId;
	}
	public void setNetizenId(String netizenId) {
		this.netizenId = netizenId;
	}
	public String getAlarmStatus() {
		return alarmStatus;
	}
	public void setAlarmStatus(String alarmStatus) {
		this.alarmStatus = alarmStatus;
	}
	public String getAlarmWords() {
		return alarmWords;
	}
	public void setAlarmWords(String alarmWords) {
		this.alarmWords = alarmWords;
	}
	
	
	
	
}
